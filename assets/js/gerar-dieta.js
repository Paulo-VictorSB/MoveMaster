// Dicas de substituições
// Pães: Substitua pão francês por pão integral ou tapioca.
// Arroz branco: Troque por arroz integral ou quinoa.
// Carnes: Varie entre frango, peixe (tilápia, salmão) e carne magra.
// Leites e derivados: Use versões desnatadas para reduzir calorias ou integrais para aumentar calorias.
// Lanches rápidos: Barras de proteína podem ser trocadas por mix de castanhas ou frutas com whey.
// Molhos e temperos: Use azeite e limão no lugar de molhos prontos.

